n1 = int(input("Введите первое целое число: "))
n2 = int(input("Введите второе целое число: "))
n3 = int(input("Введите третье целое число: "))

сумма = n1 + n2 + n3
произведение = n1 * n2 * n3
среднее = (n1 + n2 + n3) / 3

print("Сумма чисел:", сумма)
print("Произведение чисел:", произведение)
print("Среднее арифметическое чисел:", среднее)